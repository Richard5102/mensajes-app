package com.messages.Util;

public class Message {

    int id_mensage;
    String mensage;
    String autorMensage;
    String message_time;


    public int getId_mensage() {
        return id_mensage;
    }

    public void setId_mensage(int id_mensage) {
        this.id_mensage = id_mensage;
    }

    public String getMensage() {
        return mensage;
    }

    public void setMensage(String mensage) {
        this.mensage = mensage;
    }

    public String getAutorMensage() {
        return autorMensage;
    }

    public void setAutorMensage(String autorMensage) {
        this.autorMensage = autorMensage;
    }

    public String getMessage_time() {
        return message_time;
    }

    public void setMessage_time(String message_time) {
        this.message_time = message_time;
    }


}
